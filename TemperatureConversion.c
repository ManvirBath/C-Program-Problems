#include <stdio.h>

float celsius(int fahrenheit) {
    //calculations for acheiving conversion of celsius to farhenheit
    return (fahrenheit - 32) * 5.0 / 9;
}

float farhenheit(int celsius) {
    //calculations for achieving conversion of farhenheit to celsius
    return celsius * 9.0 / 5 + 32;
}

int main() {
    int i, j;

    printf("Farhenheit to Celsius conversion:\n");
    //iterates from 0 to 100
    for (i = 0; i < 101; i++) {
        printf("----------------\n");
        printf("|%d|\t |%.1f|\t\n", i, celsius(i));
        printf("----------------\n");
    }
    printf("\nCelsius to Farhenheit conversion:\n");
    //iterates from 32 to 212
    for (j = 32; j <= 212; j++) {
        printf("---------------\n");
        printf("|%d|\t |%.1f|\t\n", j, farhenheit(j));
        printf("---------------\n");
    }
}

